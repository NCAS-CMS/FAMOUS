!====================== COMDECK AMAXSIZE ========================
! Description
!   This comdeck provides parameters giving the maximum likely sizes
!   of key UM resolution variables, useful for sizing static arrays.
!
!   History:
!   Model    Date     Modification history
!  version
!   4.2      18/11/96 New comdeck created.  P.Burton
!   4.3      24/01/97 Define MaxFieldSize to be a quarter of the
!                     SHMEM common block size.         P.Burton
!   4.4      3/7/97   Add MaxFieldSizeMes. Deborah Salmond
!   4.5     12/01/98  Added new variables, and changed sizes to
!                     correspond to global hi-res forecast - current
!                     largest configuration.                P.Burton
!                     Changed MAX_SHMEM_COMMON_SIZE to 3000000
!                     required for operational data assimilation.
!                                                           P.Burton

      INTEGER

     &  ROW_LENGTH_MAX  ! Maximum row length
     &, P_ROWS_MAX      ! Maximum number of rows
     &, HORIZ_DIM_MAX   ! MAX(ROW_LENGTH_MAX,P_ROWS_MAX)
     &, HALO_MAX        ! Maximum MPP halo width
     &, P_LEVELS_MAX    ! Maximum number of total levels
     &, Q_LEVELS_MAX    ! Maximum number of wet levels

      PARAMETER ( ROW_LENGTH_MAX = 432
     &,           P_ROWS_MAX = 325
     &,           HORIZ_DIM_MAX = 432
     &,           HALO_MAX = 2  ! fourth order double width halo
     &,           P_LEVELS_MAX = 42
     &,           Q_LEVELS_MAX = 42)

! Derived sizes

      INTEGER
     &  Max2DFieldSize
     &, Max3DFieldSize
     &, MaxHaloSize

      PARAMETER (
     &  Max2DFieldSize = ROW_LENGTH_MAX*P_ROWS_MAX
     &, Max3DFieldSize = ROW_LENGTH_MAX*P_ROWS_MAX*P_LEVELS_MAX
     &, MaxHaloSize = HORIZ_DIM_MAX*HALO_MAX
     & )

      INTEGER
     &  MAX_SHMEM_COMMON_SIZE,
     &  MaxFieldSize,
     &  MaxFieldSizeMes                                                 
      PARAMETER ( MAX_SHMEM_COMMON_SIZE = 3000000 ,
     &            MaxFieldSize   = MAX_SHMEM_COMMON_SIZE/4 ,
     &            MaxFieldSizeMes= MAX_SHMEM_COMMON_SIZE/6 )
C     Local variables

      REAL
     &       NP_MEAN(P_LEVELS_MAX)   ! NP mean value
     &,      SP_MEAN(P_LEVELS_MAX)   ! SP mean value

      INTEGER
     &       ITEM,        ! STASH item number
     &       FIELD,       ! Length of individual diagnostic field
     &       I,           !
     &       J,           ! LAM E boundary pointer
     &       K,           !
     &       INDEX,       ! Position within STASH list, refering to
C                         ! current ITEM
     &       LEVELS,      !
     &       START        ! Position of first point of field at level
C                         ! to  be processed.

      INTEGER
     &  gr         ! grid type of field being processed
     &, fld_type   ! field type (P or U) of the field being processed
     &, sp_row_start     ! start point of south pole
     &, np_row_start     ! start point of north pole
     &, lvl,lsl,lso,nv,info  ! arguments to RVECSUMR

! Functions:
      INTEGER
     & EXPPXI,GET_FLD_TYPE


      DO ITEM=I1,I2
        IF(SF(ITEM)) THEN

CL Calculate number of levels at which each diagnostic is held

          INDEX=STINDEX(1,ITEM)

          IF(STLIST(10,INDEX).LT.0) THEN

C Levels list present

            LEVELS=STASH_LEVELS(1,-STLIST(10,INDEX))
          ELSEIF(STLIST(10,INDEX).LT.100) THEN

C Top and bottom levels

            LEVELS=STLIST(11,INDEX)-STLIST(10,INDEX)+1
            IF(LEVELS.LE.0) THEN
              ICODE=1
              CMESSAGE='EXTDIAG: Illegal levels limits.'
              WRITE(6,*)'EXTDIAG illegal levels, STLIST(10,11)=',
     &        STLIST(10,INDEX),STLIST(11,INDEX)
       WRITE(6,*)' SECTION AND ITEM NUMBER ',
     *     STLIST(2,INDEX),STLIST(1,INDEX)
              RETURN
            END IF
          ELSE
            LEVELS=1
          END IF

C The 26th entry of STLIST indicates if the diagnostic is input to STASH
C   on pseudo-levels and, if so, which - zero for none, otherwise the
C   number of the list of pseudo-levels the data will be input to STASH
C   on, and the first element of this list is the total number in it.
C   (NUM_PSEUDO_LEVELS being the maximum value over all pseudo-levels
C   lists.)  So LEVELS, which is currently the number of physical levels
C   the data is on, must be multiplied by this to give the number of
C   combinations of physical levels and pseudo-levels.

          IF ( STLIST(26,INDEX) .GT. 0 )
     &      LEVELS = LEVELS * STASH_PSEUDO_LEVELS(1,STLIST(26,INDEX))

CL Define length of field

! Find out the gridtype of the field
          GR = EXPPXI(im_ident,IS,ITEM,ppx_grid_type,
! COMDECK ARGPPX     
! Description:
!   Argument comdeck for passing arrays which hold the
!   ppxref information.
!
! Current code owner: S.J.Swarbrick
!
! History:
! Version   Date      Comment
! -------   ----      -------
! 3.5       Apr. 95   Original code.  S.J.Swarbrick
! 4.0       Oct. 95                   S.J.Swarbrick
! 4.4  03/11/97  Removed MKPPXRF *DEF reference. K Rogers
! 4.4  04/11/97  Changed -RECON def line to allow for other small
!                execs which had used the RECON def. K Rogers
!
     & PPXI,PPXC,ppxRecs,
     & PPXPTR,    
! End of comdeck
     &                ICODE,CMESSAGE)

          IF (ICODE .GT. 0) GOTO 9999

! and use this to find the field type (p field or u field)

          fld_type=GET_FLD_TYPE(GR)

          IF (atbase) THEN
            IF (fld_type .EQ. fld_type_p) THEN
              sp_row_start=(lasize(2)-Offy-2)*ROW_LENGTH+Offx+1
            ELSEIF (fld_type .EQ. fld_type_u) THEN
              sp_row_start=(lasize(2)-Offy-3)*ROW_LENGTH+Offx+1
            ELSE
              ICODE=1
              CMESSAGE='EXTDIAG : Unrecognized field type'
              GOTO 9999
            ENDIF
          ENDIF
          IF (attop)
     &      np_row_start=(Offy+1)*ROW_LENGTH+1+Offx

          FIELD=lasize(1)*lasize(2)


CL Extend diagnostic to full field

          DO K=1,LEVELS
            START=FIELD*(K-1)+SI(ITEM)-1

CL Copy diagnostic information to N and S boundaries

! Update Northern boundary / Pole
            IF (attop) THEN
!L Initialise unused row
! *DIR$ CACHE_BYPASS DATA
              DO I=1,Offy*ROW_LENGTH
                DATA(START+I)=0.
              ENDDO

              DO I=Offy*ROW_LENGTH+1,(Offy+1)*ROW_LENGTH
                DATA(START+I)=DATA(START+I+ROW_LENGTH)
              ENDDO
            ENDIF

! Update Southern boundary / Pole
            IF (atbase) THEN
!L Initialise unused row
! *DIR$ CACHE_BYPASS DATA
              DO I=sp_row_start+2*ROW_LENGTH-Offx+1,
     &             lasize(1)*lasize(2)
                DATA(START+I)=0.
              ENDDO

              DO I=sp_row_start-Offx+ROW_LENGTH,
     &             sp_row_start+2*ROW_LENGTH-Offx
                DATA(START+I)=DATA(START+I-ROW_LENGTH)
              ENDDO
            ENDIF

! Update Western boundary
            IF (atleft) THEN
              DO I=Offx+1,FIELD,ROW_LENGTH
                DATA(START+I)=DATA(START+I+1)
              ENDDO
            ENDIF

! Update Eastern boundary
            IF (atright) THEN
              DO I=ROW_LENGTH-Offx,FIELD,ROW_LENGTH
                DATA(START+I)=DATA(START+I-1)
              ENDDO
            ENDIF


          END DO
        END IF
      END DO

 9999 CONTINUE
      RETURN
      END
